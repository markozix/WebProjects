package model;

public class Asistent {
	
	
	private String name;
	private int ocjena;
	public Asistent(String name, int ocjena) {
		super();
		this.name = name;
		this.ocjena = ocjena;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getOcjena() {
		return ocjena;
	}
	public void setOcjena(int ocjena) {
		this.ocjena = ocjena;
	}
	
	
	

}
